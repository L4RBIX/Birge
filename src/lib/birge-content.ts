export function getDealById(id: string) {
  return deals.find((item) => item.id === id);
}

export function getDealsByCategory(category?: string) {
  if (!category || category === "all") return deals;
  return deals.filter((item) => item.category === category);
}

export function getDealRails(category?: string, mlRankedDeals?: Deal[]) {
  const scoped = getDealsByCategory(category);
  const rankedScoped = mlRankedDeals && mlRankedDeals.length > 0 ? mlRankedDeals : scoped;
  if (category && category !== "all") {
    return [
      {
        heading: `${categoryLabels[category as DealCategory] ?? "Категория"} · сделки`,
        ctaLabel: "Все сделки",
        ctaHref: "/#deals",
        products: rankedScoped,
      },
      {
        heading: "Почти собрались",
        ctaLabel: "Вступить",
        ctaHref: "#deal-detail",
        products: [...scoped].sort((a, b) => b.participants / b.target - a.participants / a.target).slice(0, 4),
      },
    ];
  }

  return [
    {
      heading: "Рекомендовано вам",
      ctaLabel: "Все сделки",
      ctaHref: "#deals",
      products:
        mlRankedDeals && mlRankedDeals.length > 0
          ? mlRankedDeals.slice(0, 8)
          : [...deals].sort((a, b) => b.recommendationSignals.score - a.recommendationSignals.score).slice(0, 8),
    },
    {
      heading: "Горячие сделки",
      ctaLabel: "Смотреть",
      ctaHref: "#deals",
      products: [...deals].sort((a, b) => b.participants / b.target - a.participants / a.target).slice(0, 8),
    },
    {
      heading: "В вашем городе",
      ctaLabel: "Алматы",
      ctaHref: "#deals",
      products: deals.filter((item) => item.city === "Алматы").slice(0, 8),
    },
    {
      heading: "Почти собрались",
      ctaLabel: "Вступить",
      ctaHref: "#deal-detail",
      products: deals.filter((item) => item.target - item.participants <= 6).slice(0, 8),
    },
  ];
}

