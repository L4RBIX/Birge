function deal(seed: DealSeed): Deal {
  const imageFile = dealVisualMap[seed.id];
  if (!imageFile) {
    throw new Error(`Missing product photo mapping for deal: ${seed.id}`);
  }
  const imagePath = `/images/products/${imageFile}`;
  return {
    ...seed,
    image: imagePath,
    image480: imagePath,
    categoryLabel: categoryLabels[seed.category],
    visual: "photo",
    visualTone: seed.tone,
    recommendationSignals: recommendationSignals(seed.category, seed.city, seed.finalPrice, seed.participants, seed.target),
    tiers: tiers(seed.target, seed.discount),
    escrowNotes,
    href: `/deal/${seed.id}`,
  };
}

