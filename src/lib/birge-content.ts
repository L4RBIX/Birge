function tiers(currentTarget: number, discount: number): PriceTier[] {
  if (currentTarget <= 10) {
    return [
      { people: 5, discount: 12, status: "unlocked" },
      { people: 10, discount, status: "current" },
      { people: 25, discount: Math.min(45, discount + 8), status: "locked" },
    ];
  }

  if (currentTarget >= 50) {
    return [
      { people: 20, discount: 18, status: "unlocked" },
      { people: 50, discount, status: "current" },
      { people: 100, discount: Math.min(46, discount + 10), status: "locked" },
    ];
  }

  return [
    { people: 10, discount: 15, status: "unlocked" },
    { people: 20, discount, status: "current" },
    { people: 50, discount: Math.min(42, discount + 9), status: "locked" },
  ];
}

function recommendationSignals(category: DealCategory, city: Deal["city"], finalPrice: number, participants: number, target: number) {
  const interestMatch = ["electronics", "home", "beauty"].includes(category) ? 1 : category === "kids" ? 0.75 : 0.55;
  const budgetFit = finalPrice <= 50000 ? 1 : finalPrice <= 90000 ? 0.75 : 0.45;
  const cityMatch = city === "Алматы" ? 1 : 0.55;
  const momentum = Math.min(1, participants / target);
  const score = Number((0.4 * interestMatch + 0.2 * budgetFit + 0.2 * cityMatch + 0.2 * momentum).toFixed(2));
  return { interestMatch, budgetFit, cityMatch, momentum: Number(momentum.toFixed(2)), score };
}

